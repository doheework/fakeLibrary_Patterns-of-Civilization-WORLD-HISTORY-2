$(document).ready(function(){
    $(".칠판").mouseover(function(){
        var image = $(".칠판i");
    $(".칠판i").show();
    $(".칠판i").stop().animate({top:window.innerHeight/2-image.innerHeight()/2});
    });
    $(".칠판").mouseout(function(){
    $(".칠판i").stop().animate({top:"100%"});
    
    });
    });

